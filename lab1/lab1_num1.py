# Запрашиваем у пользователя три числа
a = int(input('Введите первое число: '))
b = int(input('Введите второе число: '))
c = int(input('Введите третье число: '))
# Ввыводим максимальное и минимальное значение
print(max(a,b,c))
print(min(a,b,c))
# Проверяем с помощью if равенство чисел
if a==b==c:
  print('Они равны')
else:
  print("Они не равны")
